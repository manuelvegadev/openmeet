function drawText(buf: Buffer, bufW: number, bufH: number, text: string, startX: number, startY: number): void {
  for (let i = 0; i < text.length; i++) {
    const code = text.charCodeAt(i);
    if (code < 32 || code > 126) continue;

    const glyphOff = (code - FONT_FIRST) * FONT_H;
    const cx = startX + i * CHAR_W;
    if (cx + CHAR_W > bufW) break;

    for (let gy = 0; gy < FONT_H; gy++) {
      const row = FONT[glyphOff + gy];
      for (let gx = 0; gx < FONT_W; gx++) {
        if (row & (0x80 >> gx)) {
          // Draw 2×2 block (SCALE=2) with Y=235 (white)
          const px = cx + gx * SCALE;
          const py = startY + gy * SCALE;
          if (py + 1 < bufH) {
            const off0 = py * bufW + px;
            const off1 = off0 + bufW;
            buf[off0] = 235;
            buf[off0 + 1] = 235;
            buf[off1] = 235;
            buf[off1 + 1] = 235;
          }
        }
      }
    }
  }
}

/**
 * Render a semi-transparent dark bar with white text at the bottom of an I420 frame.
 * Shows peer name, stream type, and source resolution.
 */
export function renderOverlay(
  buf: Buffer,
  w: number,
  h: number,
  peerName: string,
  streamType: string,
  srcW: number,
  srcH: number,
): void {
  // Build text — filter to ASCII-renderable chars (emoji usernames become "peer")
  const name = peerName.replace(/[^\x20-\x7E]/g, '').trim() || 'peer';
  const text = `${name} | ${streamType} | ${srcW}x${srcH}`;

  const barY = h - BAR_H;

  // Darken Y plane in bar area (multiply by ~0.3)
  for (let y = barY; y < h; y++) {
    const row = y * w;
    for (let x = 0; x < w; x++) {
      buf[row + x] = (buf[row + x] * 77) >> 8;
    }
  }

  // Desaturate U/V planes in bar area (blend toward neutral 128)
  const halfW = w >> 1;
  const halfH = h >> 1;
  const uOff = w * h;
  const vOff = uOff + halfW * halfH;
  const barHalfY = barY >> 1;

  for (let y = barHalfY; y < halfH; y++) {
    const row = y * halfW;
    for (let x = 0; x < halfW; x++) {
      buf[uOff + row + x] = (buf[uOff + row + x] + 128) >> 1;
      buf[vOff + row + x] = (buf[vOff + row + x] + 128) >> 1;
    }
  }

  // Draw white text on the dark bar
  drawText(buf, w, h, text, PAD_X, barY + PAD_Y);
}
